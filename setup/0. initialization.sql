--delete from dbo.Product
DECLARE @count int  = 1
IF NOT EXISTS(SELECT SerialNumber FROM dbo.Product)
BEGIN
	WHILE @count < 101
	BEGIN
		INSERT INTO dbo.Product(SerialNumber)
		VALUES ('SN' + UPPER(CONVERT(varchar(36), NEWID())))
		SET @count += 1
	END
END
GO

